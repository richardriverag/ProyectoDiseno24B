    private void cambiarImagen(String opcion) {
        //Diccionario de imágenes 
        String rutaImagen = switch (opcion) {
            case "Piscina" -> "/imagenes/imagenesEspaciosComunes/piscina.png";
            case "Barbacoa" -> "/imagenes/imagenesEspaciosComunes/barbacoa.jpg";
            case "Gimnasio" -> "/imagenes/imagenesEspaciosComunes/gimnasio.jpg";
            case "Canchas" -> "/imagenes/imagenesEspaciosComunes/canchas.jpg";  
            case "Salon" -> "/imagenes/imagenesEspaciosComunes/salon.jpg";    
            default -> null;
        };
        if (rutaImagen != null) {
        // Cargar la imagen como recurso
        java.net.URL imgURL = getClass().getResource(rutaImagen);
        if (imgURL != null) {
            ImageIcon icon = new ImageIcon(imgURL);
            Image img = icon.getImage();
            lblImagen.setIcon(new ImageIcon(img));
        } else {
            System.out.println("No se encontró la imagen: " + rutaImagen);
        }
    }
  }
